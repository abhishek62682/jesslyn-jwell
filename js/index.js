let activeCategory = "rings";

function renderTabs() {
  document.querySelectorAll(".product-filter").forEach((tab) => {
    const tabName = tab.childNodes[0].textContent.trim().toLowerCase();
    const cat = PRODUCT_LIST.categories.find((c) => c.category === tabName);
    const isActive = tabName === activeCategory;

    tab.classList.toggle("active", isActive);
    tab.classList.toggle("inactive", !isActive);

    // Update count
    if (cat) tab.querySelector("span").textContent = cat.items.length;
  });
}

function renderGrid() {
  const cat = PRODUCT_LIST.categories.find(
    (c) => c.category === activeCategory,
  );
  const grid = document.querySelector(".product-listing");

  grid.innerHTML = cat.items
    .map(
      (item) => `
    <div class="product-item w-full rounded-[6px] p-6 bg-[#f3eee4]">
      <div class="w-full w-md mx-auto">
      
      <img class="w-full" src="${item.image}" alt="${item.name}" />
      
      </div>
      <div class="product-item-content mt-6 flex justify-between items-end">
        <div class="product-label-wrapper uppercase font-primary text-md leading-md">
          <div class="product-label-name">${item.name}</div>
          <div class="product-label-materials text-cblack">${item.material}</div>
        </div>
        <div class="product-label-price text-[#141313] text-sm leading-sm font-primary uppercase">
          <span class="currency">Rs</span>
          <span class="amount">${item.price.toLocaleString()}</span>
        </div>
      </div>
    </div>
  `,
    )
    .join("");
}

document.querySelectorAll(".product-filter").forEach((tab) => {
  tab.addEventListener("click", () => {
    activeCategory = tab.childNodes[0].textContent.trim().toLowerCase();
    renderTabs();
    renderGrid();
  });
});

renderTabs();
renderGrid();
